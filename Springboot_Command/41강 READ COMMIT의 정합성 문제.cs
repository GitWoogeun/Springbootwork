
# READ COMMIT의 정합성 유저

하나의 Database에서 
a가 name 컬럼의 임꺽정 컬럼값을 장보고라고 Update를 실행을 했다
b가 select로 name컬럼의 장보고을 조회를 할려고 했다.
하지만 b가 아무리 select를 날린다고 하여도 임꺽정의 컬럼값만 조회가 된다.
이때 a가 update문을 수행하고 난 뒤 commit을 할 시 
이제서야 b가 select로 장보고 정보를 조회를 할 수 있게 되었다.

