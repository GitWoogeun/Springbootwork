# READ COMMIT은 ( SELECT 트랜잭션 )
# 데이터의 정확성이 깨진다.
SELECT를 하는 트랜잭션을 시작 했을 때 그 트랜잭션이 끝날때까지는
데이터의 변경이 일어나면 안되는데 갑자기 외부에서 트랜잭션 중
끼어들어서 데이터의 변경을 일으켜서 데이터가 변경이 된다면
데이터의 정확성이 깨진다라고 말할 수 있습니다.

이를 해결할려면 

repeatable read 방식을 사용해야 합니다.

