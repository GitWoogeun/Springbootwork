# MINE타입 :
=> MIME 타입이란 클라이언트에게 전송된 문서의 다양성을 알려주기 위한 메커니즘입니다;
1. Text, 2. JPEG, 3. AVI, 4. 이진데이터, 5. JSON데이터 등등 있다.

# MINE타입 문법 
=> TYPE/SUBTYPE
EX)
text/plain
text/html
image/jpeg
image/png
audio/mpeg
audio/ogg
audio
video/mp4
application/octet-stream
