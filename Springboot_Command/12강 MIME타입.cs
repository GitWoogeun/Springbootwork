# 패킷 스위칭 방식 :
=> A와 B가 통신을할 때, 가,나,다,라 데이터를 보낸다고 할 떄
   가 1개, 나1개, 다1개, 라1개 (쪼개진 데이터 하나 : 패킷)
   쪼개진 4개의 패킷 데이터를 하나씩 올려서(스트림(파이프))에 올려서 B에게 보낸다.
   => 선이 연결되지 않고 기존 선에 같이 달라 붙어서 요청이 된다.

단점 : 데이터가 하나의 선을 공유해서 쓰게 되면 데이터가 뒤죽박죽이 되어서
      B가 데이터를 받을 때 섞인 데이터들이 한번에 받기 때문에 조립을 해야한다.
      ( 데이터의 순서가 꼬일수있다. )
      모든 패킷에는 헤더가 붙어서 전달해야한다 ( 헤더에는 데이터의 순서가 붙을 수 있다. )

# 서킷 스위칭 방식 :
A와 B가 데이터를 교환할 때, 데이터를 한방에 실어서 스트림(파이프)에 올려서. B에게 전송
 단점 : 선이 추가적으로 계속 연결이 되니까 데이터 요청 속도는 빠르다 하지만, 
        물리적으로 선이 계속 연결되어야하니까 비용이 많이 든다.;


